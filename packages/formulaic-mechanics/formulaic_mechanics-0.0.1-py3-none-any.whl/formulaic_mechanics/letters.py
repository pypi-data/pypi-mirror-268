def make_auto_diphthongs():
    dipthongs = []
    for i in iota_lower:
        dipthongs.append('α' + i)
        dipthongs.append('ε' + i)
        dipthongs.append('ο' + i)
        dipthongs.append('υ' + i)
    for u in upsi_lower:
        dipthongs.append('α' + u)
        dipthongs.append('ε' + u)
        dipthongs.append('ο' + u)
        dipthongs.append('η' + u)
        dipthongs.append('ω' + u)
    for I in iota_upper:
        dipthongs.append('Α' + I)
        dipthongs.append('Ε' + I)
        dipthongs.append('Ο' + I)
