def add(a, b):
    return a + b

def sub(a, b):
    return a - b

def multiply(a, b):
    return a * b

def div(a, b):
    return a / b

def rem(a, b):
    return a % b

def power(a, b):
    return a ** b

def square_root(a):
    return pow(a, 0.5)

def absolute(a):
    return abs(a)

def floor_div(a, b):
    return a // b  

def ceil_div(a, b):
    return -(-a // b)
