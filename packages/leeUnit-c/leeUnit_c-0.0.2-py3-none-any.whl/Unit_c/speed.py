# 킬로미터/시 <-> 마일/시
def kph_mph(kph):
    return kph * 0.621371

def mph_kph(mph):
    return mph / 0.621371

# 미터/초 <-> 킬로미터/시
def mps_kph(mps):
    return mps * 3.6

def kph_mps(kph):
    return kph / 3.6

