# 킬로미터 <-> 마일
def kilometer_mile(kilometer):
    return kilometer * 0.621371

def mile_kilometer(mile):
    return mile / 0.621371

# 미터 <-> 피트
def meter_feet(meter):
    return meter * 3.28084

def feet_meter(feet):
    return feet / 3.28084

# 미터 <-> 센티미터
def meter_centimeter(meter):
    return meter * 100

def centimeter_meter(centimeter):
    return centimeter / 100

# 피트 <-> 인치
def feet_inch(feet):
    return feet * 12

def inch_feet(inch):
    return inch / 12

# 인치 <-> 센티미터
def inch_centimeter(inch):
    return inch * 2.54

def centimeter_inch(centimeter):
    return centimeter / 2.54

# 마일 <-> 야드
def mile_yard(mile):
    return mile * 1760

def yard_mile(yard):
    return yard / 1760

# 미터 <-> 야드
def meter_yard(meter):
    return meter * 1.09361

def yard_meter(yard):
    return yard / 1.09361