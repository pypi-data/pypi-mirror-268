def mingzi(volume=1, female_rate=0.5, single_rate=0.5):
    data = {}
    for i in range(volume):
        surname = rand_surname()
        length = random() > single_rate
        gender = random() > female_rate
        if gender:
            if length:
                name = surname + choice(male_single)
            else:
                name = surname + choice(male_classical)
        else:
            if length:
                name = surname + choice(female_single)
            else:
                name = surname + choice(female_classical)
        data[name] = ["女", "男"][gender]
    return data
